# Mathimatical Operators
# +, - , *, /, //, **, %
print("5" + "5")
print(-5 - 6)
print(5 * "Hello")
print(6/5)
print(6//5)
print(6**2)
print(6%5)


# Comparision operators
# > < >= <= ==
print(5 > 7)
print(5 < 7)
print(5 >= 5)
print(5 <= 7)
print(5 == 5)


# Logical Operators
# and, or, not
print(5 > 8 and 5 < 8)
print( 6 > 4 or 6 == 4)
print(not True)


